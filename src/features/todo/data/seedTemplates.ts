export function getTemplateById(id: string): TodoTemplate | undefined {
  return SEED_TEMPLATES.find((t) => t.id === id);
}

export function getTemplateCategories(): TemplateCategory[] {
  // Keep the canonical ordering, but only surface categories that have templates.
  const present = new Set(SEED_TEMPLATES.map((t) => t.category));
  return TEMPLATE_CATEGORIES.filter((c) => present.has(c));
}

export function getTemplatesByCategory(category: TemplateCategory): TodoTemplate[] {
  return SEED_TEMPLATES.filter((t) => t.category === category);
}

export function getFeaturedTemplates(): TodoTemplate[] {
  return SEED_TEMPLATES.filter((t) => t.featured);
}

export function searchTemplates(query: string): TodoTemplate[] {
  const q = query.trim().toLowerCase();
  if (!q) return SEED_TEMPLATES;
  return SEED_TEMPLATES.filter((t) => {
    const haystack = [t.name, t.description, t.category, ...t.tasks.flatMap((task) => task.tags ?? [])]
      .join(" ")
      .toLowerCase();
    return haystack.includes(q);
  });
}
